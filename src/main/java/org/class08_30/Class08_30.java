package org.class08_30;

public class Class08_30 {
    public static void main(String[] args) {
        int n = 9;
        for (int i = 1; i < n/2; i++) {
            if (n%i == 0) {
                if (i == n/i) {
                    System.out.println(i);
                } else {
                System.out.println(i);
                System.out.println(n/i);
                }
            }
        }
    }
}
